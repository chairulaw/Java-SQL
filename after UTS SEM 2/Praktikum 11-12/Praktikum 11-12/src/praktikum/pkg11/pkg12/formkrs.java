    private void txtnimKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnimKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==10){
            try{
                koneksi();
                String sql = "Select * from mahasiswa_2221026 where NIMmhs='"+txtnim.getText()+"'";
                ResultSet rs = cn.executeQuery(sql);
                
                if (rs.next()){
                    txtnama.setText(rs.getString(2));
                    txtkodemtk.requestFocus();
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Data Mahasiswa Tidak Ada");
                    txtnim.requestFocus();
                }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Ada Kesalahan Menampilkan Data Mahasiswa" + e);
            }
        }     
    }//GEN-LAST:event_txtnimKeyPressed

