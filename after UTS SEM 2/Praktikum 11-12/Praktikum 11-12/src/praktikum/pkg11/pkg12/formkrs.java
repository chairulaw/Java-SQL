            void cekkrs(){
            DefaultTableModel tabelnyo=new DefaultTableModel();
            tabelnyo.addColumn("Kode");
            tabelnyo.addColumn("Nama Matakuliah");
            tabelnyo.addColumn("SKS");

            try{
                koneksi();
                String sql = "SELECT NO_KRS,KRS_SEMESTER,KRS_KDMTK,"
                        + "KRS_NIM,Namamhs,NILAI,NILAI,NILAI "
                        + "FROM krs join mahasiswa on KRS_NIM=NIMmhs "
                        + "join matakuliah on KRS_KDMTK=KODE_MTK"
                        +  "  WHERE  KRS_SEMESTER='"+  txtsem.getText()  +"'  AND  KRS_SEMESTER='"
                        + txtnim.getText() +"'";
                ResultSet rs = cn.executeQuery(sql);
                while (rs.next()) {
                    tabelnyo.addRow(new Object[]{
                        rs.getString(3),
                        rs.getString(7),
                        rs.getString(8)
                    });
                }
                jTable2.setModel(tabelnyo);
            }catch (Exception e){
                JOptionPane.showMessageDialog(null,"Kesalahan Menampilkan Data"+ e);
            }
        }
            
