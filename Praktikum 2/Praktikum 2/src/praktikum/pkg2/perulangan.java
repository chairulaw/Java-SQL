public class perulangan {
    public static void main(String[] args) {
    //perulangan for
    int i;
    for (i=1;i<6;i++){
    System.out.println(i);
    }
    
    //perulangan while
    int j=8;
    while(j>0){
    System.out.print(j);
    j--;
}
    
    //perulangan do while
    int x = 0;
    do
    {
    System.out.println(x);
    x++;
    }while (x<11);
}
}