public class penggabunganstring {
   public static void main(String[] args) {
   //Dua string dapat digabungkan menjadi satu string panjang
   System.out.println("Berikut disajikan beberapa fakta kehidupan yang kamu "
            +"perlu anda ketahui");
   System.out.println();
   
   //Sebuah String dapat memuat digit-digit
   System.out.println("Jumlah huruf dalam alphabet Hawaiin :12");
   
   //Sebuah nilai numeric dapat disambung dengan String
   System.out.println("Kode Telpon Antartica : +672");
   System.out.println("Tahun dimana Leonardo Da Vinci menciptakan" 
           + "The Parachute : 1515");
   System.out.println("terdapat : "+24+" Jam Sehari");
   }
    
}
